int		ft_atoi(const char *s)
{
	int ret;
	int i;
	int sign;

	ret = 0;
	i = 0;
	sign = 1;
	while (s[i] == ' ' || s[i] == '\n' || s[i] == '\v'
			|| s[i] == '\t' || s[i] == '\r' || s[i] == '\f')
		i++;
	if (s[i] == '-')
	{
		sign = -1;
		i++;
	}
	else if (s[i] == '+')
		i++;
	while (s && s[i] >= '0' && s[i] <= '9')
	{
		ret = ret * 10 + (s[i] - '0');
		i++;
	}
	return (sign * ret);
}
